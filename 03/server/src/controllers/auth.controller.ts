import { CREATED, OK, UNAUTHORIZED } from "@/constants/http.js";
import SessionModel from "@/models/session.model.js";
import {
  createAccount,
  loginUser,
  refreshUserAccessToken,
  verifyEmail,
} from "@/services/auth.service.js";
import catchErrors from "@/utils/catchErrors.js";
import {
  clearAuthCookies,
  getAccessTokenCookieOptions,
  getRefreshAccessTokenCookieOptions,
  setAuthCookies,
} from "@/utils/cookies.js";
import { verifyToken } from "@/utils/jwt.js";
import {
  loginSchema,
  registerSchema,
  verificationCodeSchema,
} from "./auth.schama.js";
import appAssert from "@/utils/appAssert.js";

export const registerHandler = catchErrors(async (req, res) => {
  // validate request

  const request = registerSchema.parse({
    ...req.body,
    userAgent: req.headers["user-agent"],
  });
  // call serveice
  const { user, accessToken, refreshToeken } = await createAccount(request);
  // return response

  return setAuthCookies({ res, accessToken, refreshToeken })
    .status(CREATED)
    .json(user);
});

export const loginHandler = catchErrors(async (req, res) => {
  const request = loginSchema.parse({
    ...req.body,
    userAgent: req.headers["user-agent"],
  });
  // call serveice
  const { accessToken, refreshToeken, user } = await loginUser(request);

  return setAuthCookies({ res, accessToken, refreshToeken })
    .status(OK)
    .json({ message: "Login successfull" });
});

export const logoutHandler = catchErrors(async (req, res) => {
  const accessToken = req.cookies.accessToken as string | undefined;
  const { payload, error } = verifyToken(accessToken || "");
  if (payload) {
    await SessionModel.findByIdAndDelete(payload.sessionId);
  }
  return clearAuthCookies(res).status(OK).json({
    message: "Logout successfull",
  });
});

export const refreshHandler = catchErrors(async (req, res) => {
  const refreshToken = req.cookies.refreshToken as string | undefined;
  appAssert(refreshToken, UNAUTHORIZED, "Missing refresh token.");

  // call service
  const { accessToken, newRefreshToken } =
    await refreshUserAccessToken(refreshToken);

  if (newRefreshToken) {
    res.cookie(
      "refreshToken",
      newRefreshToken,
      getRefreshAccessTokenCookieOptions(),
    );
  }
  return res
    .status(OK)
    .cookie("accessToken", accessToken, getAccessTokenCookieOptions())
    .json({
      message: "Access token refreshed",
    });
});

export const verifyEmailHandler = catchErrors(async (req, res) => {
  const verificationCode = verificationCodeSchema.parse(req.params.code);
  // call service
  const { user } = await verifyEmail(verificationCode);
  return res.status(OK).json({
    message: "Email was successfully verified",
  });
});
